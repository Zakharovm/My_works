public class Triangle {
    private final double a;
    private final double b;
    private final double c;

    public Triangle(final double s1, final double s2, final double s3) {
        a = s1;
        b = s2;
        c = s3;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public double getC() {
        return c;
    }
}
